package Model;

public class DebitCard extends Card{
	private String bankName;
	private String ditributionDate;
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getDitributionDate() {
		return ditributionDate;
	}
	public void setDitributionDate(String ditributionDate) {
		this.ditributionDate = ditributionDate;
	}
	
	
}
